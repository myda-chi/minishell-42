#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*str;

	if (!s || !f)
		return (NULL);
	i = 0;
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
char to_uppercase(unsigned int index, char c)
{
    (void)index;
	return(ft_toupper(c));
}
#include <stdio.h>
int main()
{
    char ptr[]="hello";
    printf("%s", ft_strmapi(ptr, to_uppercase));
}